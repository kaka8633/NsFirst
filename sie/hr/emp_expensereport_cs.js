/**
 * The recordType (internal id) corresponds to the "Applied To" record in your
 * script deployment.
 * 
 * @appliedtorecord recordType
 * 
 * @param {String}
 *            type Sublist internal id
 * @param {String}
 *            name Field internal id
 * @param {Number}
 *            linenum Optional line item number, starts from 1
 * @returns {Boolean} True to continue changing field value, false to abort
 *          value change
 */
function clientValidateField(type, name, linenum) {

	// console.info('type=' + type);
	// console.info('name=' + name);
	// console.info('linenum=' + linenum);

	// 费用行第一行选择客户后带出客户项目到费用报告头及关联出项目经理的主管 20181009
	if (type == 'expense' && linenum == '1' && name == 'customer') {

		var customer = nlapiGetCurrentLineItemValue(type, name);// 项目
		var managerId = nlapiLookupField('job', customer,
				'custentity_project_manager');// 项目经理
		var empId = getEmpManager(managerId);// 项目经理的主管

		nlapiSetFieldValue('custbody_project_name', customer, true, true);
		nlapiSetFieldValue('custbody_project_manager_governor', empId, true, true);

	}

	return true;
}

