/**
 * The recordType (internal id) corresponds to the "Applied To" record in your
 * script deployment.
 * 
 * @appliedtorecord recordType
 * 
 * @param {String}
 *            type Sublist internal id
 * @param {String}
 *            name Field internal id
 * @param {Number}
 *            linenum Optional line item number, starts from 1
 * @returns {Boolean} True to continue changing field value, false to abort
 *          value change
 */
function clientValidateField(type, name, linenum) {
	// 获取通用项目配置文件
	var configValue = parseInt(getConfigValue('EMP_TIMESHEET_CUSTOMER'));
	// 获取机会
	var op = nlapiGetFieldValue('custcol_opportunity_list');
	// 当前项目
	var currentProject = nlapiGetFieldValue('customer');

	if (name == 'custcol_opportunity_list') {
		if (op != null && op != "") {
			nlapiSetFieldValue('customer', configValue, true, true);

			nlapiSetFieldValue('custcol_join_project', configValue, true, true);

		} else {
			nlapiSetFieldValue('customer', null, true, true);
		}
	}

	if (name == 'customer') {

		if (op == null || op == "") {

			nlapiSetFieldValue('custcol_join_project', currentProject, true,
					true);
		} else {
			return false;
		}
	}

	return true;
}

